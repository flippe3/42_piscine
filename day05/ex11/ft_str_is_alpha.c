int		alpha(int c)
{
	return (c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z');
}

int		ft_str_is_alpha(char *str)
{
	while (*str != '\0')
	{
		if (!alpha(*str))
			return (0);
		str++;
	}
	return (1);
}
